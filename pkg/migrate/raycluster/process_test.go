// --- ProcessRayClusterYAML ---

func TestProcessRayClusterYAML_FullCleanup(t *testing.T) {
	g := NewWithT(t)

	rc := newRayCluster("test", "default", withTLSHead)
	raycluster.ProcessRayClusterYAML(rc)

	head, _, _ := unstructured.NestedMap(rc.Object, "spec", "headGroupSpec")

	// enableIngress should be false
	enableIngress, _, _ := unstructured.NestedBool(head, "enableIngress")
	g.Expect(enableIngress).To(BeFalse())

	// oauth-proxy container should be removed, ray-head should remain
	containers, _, _ := unstructured.NestedSlice(head, "template", "spec", "containers")
	g.Expect(containers).To(HaveLen(1))
	c0, _ := containers[0].(map[string]any)
	g.Expect(c0["name"]).To(Equal("ray-head"))

	// TLS env vars should be removed from ray-head, MY_APP_ENV should remain
	envs, _, _ := unstructured.NestedSlice(c0, "env")
	g.Expect(envs).To(HaveLen(1))
	e0, _ := envs[0].(map[string]any)
	g.Expect(e0["name"]).To(Equal("MY_APP_ENV"))

	// TLS volume mounts should be removed, data-vol should remain
	mounts, _, _ := unstructured.NestedSlice(c0, "volumeMounts")
	g.Expect(mounts).To(HaveLen(1))
	m0, _ := mounts[0].(map[string]any)
	g.Expect(m0["name"]).To(Equal("data-vol"))

	// create-cert init container should be removed entirely
	_, initExists, _ := unstructured.NestedSlice(head, "template", "spec", "initContainers")
	g.Expect(initExists).To(BeFalse())

	// serviceAccountName should be removed
	_, saExists, _ := unstructured.NestedString(head, "template", "spec", "serviceAccountName")
	g.Expect(saExists).To(BeFalse())

	// TLS volumes removed, data-vol should remain
	vols, _, _ := unstructured.NestedSlice(head, "template", "spec", "volumes")
	g.Expect(vols).To(HaveLen(1))
	v0, _ := vols[0].(map[string]any)
	g.Expect(v0["name"]).To(Equal("data-vol"))
}

