// --- helpers ---

func newRayCluster(name, ns string, specFn func(map[string]any)) *unstructured.Unstructured {
	spec := map[string]any{}
	if specFn != nil {
		specFn(spec)
	}
	rc := &unstructured.Unstructured{
		Object: map[string]any{
			"apiVersion": "ray.io/v1",
			"kind":       "RayCluster",
			"metadata": map[string]any{
				"name":      name,
				"namespace": ns,
			},
			"spec": spec,
		},
	}

	return rc
}

